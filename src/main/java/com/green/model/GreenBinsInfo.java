public class GreenBinsInfo {

    private String geenrationPoint;
    private String material;
    private String binNumber;
    private float capacity_kg ;


    public String getGeenrationPoint() {
        return geenrationPoint;
    }

    public void setGeenrationPoint(String geenrationPoint) {
        this.geenrationPoint = geenrationPoint;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getBinNumber() {
        return binNumber;
    }

    public void setBinNumber(String binNumber) {
        this.binNumber = binNumber;
    }

    public float getCapacity_kg() {
        return capacity_kg;
    }

    public void setCapacity_kg(float capacity_kg) {
        this.capacity_kg = capacity_kg;
    }

}
